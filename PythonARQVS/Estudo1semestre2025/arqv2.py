
# Declaração de variáveis
a = 5
b = 85
c = 96

# Cálculo do resultado
resultado = (a + b) * c

# Exibe o valor da variável 'resultado'
print("O resultado do cálculo é:", resultado)

# Estrutura condicional para verificar o valor de 'resultado'
if resultado <= 100:
    print("A condição é verdadeira: o resultado é menor ou igual a 100.")
else:
    print("A condição é falsa: o resultado é maior que 100.")
