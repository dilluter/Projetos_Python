
# Declaração de variáveis
a = 10
b = 3

# Operadores lógicos
resultado_and = (a > 5) and (b < 5)  
resultado_or = (a > 15) or (b < 5)   
resultado_not = not (a > 5)          

# Exibe os resultados dos operadores lógicos
print("Resultado do operador AND:", resultado_and)
print("Resultado do operador OR:", resultado_or)
print("Resultado do operador NOT:", resultado_not)

