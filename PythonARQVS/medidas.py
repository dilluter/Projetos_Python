a = float(input("digite o valor de a: "))
b = float(input("digite o valo de b: "))
c = float(input("digite o valor de c: "))

quadrado = a*2
retangulo = b*a
triangulo = (a*b)/2
trapezio = ((a+b)*c)/2
print(f"area do quadrado: {quadrado:.2f}")
print(f"area do retangulo: {retangulo:.2f}")
print(f"area do triangulo: {triangulo:.2f}")
print(f"area do trapezio: {trapezio:.2f}")
